const mongoose = require('mongoose');

const AppointmentSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  salon: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Salon',
    required: true
  },
  services: [{
    name: String,
    price: Number
  }],
  totalPrice: {
    type: Number,
    required: true
  },
  bookingFee: {
    type: Number,
    default: 5
  },
  appointmentDate: {
    type: Date,
    required: true
  },
  estimatedStartTime: {
    type: Date,
    required: true
  },
  estimatedEndTime: {
    type: Date,
    required: true
  },
  status: {
    type: String,
    enum: ['pending', 'confirmed', 'completed', 'cancelled'],
    default: 'pending'
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('Appointment', AppointmentSchema);
























































































































































































































































































































































































































































